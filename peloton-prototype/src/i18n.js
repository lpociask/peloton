export function getInitialLocale() {
  if (typeof window === "undefined") return DEFAULT_LOCALE;
  try {
    const savedLocale = window.localStorage.getItem("peloton-locale");
    return savedLocale === "en" ? "en" : DEFAULT_LOCALE;
  } catch {
    return DEFAULT_LOCALE;
  }
}
